    public void displayData(javax.swing.JTable table) {
    try {
        Connection con = DBConnection.getConnection();
        String query = "SELECT * FROM products"; // or your table name
        PreparedStatement pst = con.prepareStatement(query);
        ResultSet rs = pst.executeQuery();

        // Fill JTable with ResultSet
        table.setModel(DbUtils.resultSetToTableModel(rs)); // Requires rs2xml.jar
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}

